package com.joinsoft.platform.global.entity;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

/**
 * 自然幢类型
 * @author LZX 2017-07-20 1340 修改使用缓存
 *
 */
public class CacheBldType implements java.io.Serializable{

	private static final long serialVersionUID = -4625594890165035300L;
	private java.lang.String zonecode;
	private java.lang.String bldtype;
	private java.lang.String describe;
	private Integer startfloor;
	private Integer endfloor;
	public java.lang.String getZonecode() {
		return zonecode;
	}
	public void setZonecode(java.lang.String zonecode) {
		this.zonecode = zonecode;
	}
	public java.lang.String getBldtype() {
		return bldtype;
	}
	public void setBldtype(java.lang.String bldtype) {
		this.bldtype = bldtype;
	}
	public java.lang.String getDescribe() {
		return describe;
	}
	public void setDescribe(java.lang.String describe) {
		this.describe = describe;
	}
	public Integer getStartfloor() {
		return startfloor;
	}
	public void setStartfloor(Integer startfloor) {
		this.startfloor = startfloor;
	}
	public Integer getEndfloor() {
		return endfloor;
	}
	public void setEndfloor(Integer endfloor) {
		this.endfloor = endfloor;
	}
	public String toString() {
		return new ToStringBuilder(this,ToStringStyle.MULTI_LINE_STYLE)
			.append("Zonecode",getZonecode())
			.append("Bldtype",getBldtype())
			.append("Describe",getDescribe())
			.append("Startfloor",getStartfloor())
			.append("Endfloor",getEndfloor())
			.toString();
	}
}
