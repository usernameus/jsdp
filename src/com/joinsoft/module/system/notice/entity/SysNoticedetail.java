package com.joinsoft.module.system.notice.entity;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;
/**
 * 通知用户详情
 * @author scy
 * @since  2018年3月30日
 */
public class SysNoticedetail{
	private java.lang.String sid;
	private java.lang.String noticeid;
	private java.lang.String userid;
	private java.lang.String viewflag;
	private java.lang.String viewtime;
	private java.lang.String remark;
	private java.lang.String corptype;


	public void setSid(java.lang.String value) {
		this.sid = value;
	}
	
	public java.lang.String getSid() {
		return this.sid;
	}
	public void setNoticeid(java.lang.String value) {
		this.noticeid = value;
	}
	
	public java.lang.String getNoticeid() {
		return this.noticeid;
	}
	public void setUserid(java.lang.String value) {
		this.userid = value;
	}
	
	public java.lang.String getUserid() {
		return this.userid;
	}
	public void setViewflag(java.lang.String value) {
		this.viewflag = value;
	}
	
	public java.lang.String getViewflag() {
		return this.viewflag;
	}
	public void setViewtime(java.lang.String value) {
		this.viewtime = value;
	}
	
	public java.lang.String getViewtime() {
		return this.viewtime;
	}
	public void setRemark(java.lang.String value) {
		this.remark = value;
	}
	
	public java.lang.String getRemark() {
		return this.remark;
	}
	public void setCorptype(java.lang.String value) {
		this.corptype = value;
	}
	
	public java.lang.String getCorptype() {
		return this.corptype;
	}


	public String toString() {
		return new ToStringBuilder(this,ToStringStyle.MULTI_LINE_STYLE)
			.append("Sid",getSid())
			.append("Noticeid",getNoticeid())
			.append("Userid",getUserid())
			.append("Viewflag",getViewflag())
			.append("Viewtime",getViewtime())
			.append("Remark",getRemark())
			.append("Corptype",getCorptype())
			.toString();
	}
}

