package com.joinsoft.module.system.zoneCode.entity;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class DzoneCode {
	private java.lang.String sid;
	private String pid;
	private java.lang.String zonecode;
	private java.lang.String zonename;
	private java.lang.String zonetype;
	private java.lang.String createby;
	private java.lang.String createtime;
	private java.lang.String updateby;
	private java.lang.String updatetime;
	private java.lang.String remark;
	private java.lang.String delflag;
	private java.lang.String orgno;

	public java.lang.String getSid() {
		return sid;
	}

	public void setSid(java.lang.String sid) {
		this.sid = sid;
	}

	public java.lang.String getZonecode() {
		return zonecode;
	}

	public void setZonecode(java.lang.String zonecode) {
		this.zonecode = zonecode;
	}

	public java.lang.String getZonename() {
		return zonename;
	}

	public void setZonename(java.lang.String zonename) {
		this.zonename = zonename;
	}

	public java.lang.String getZonetype() {
		return zonetype;
	}

	public void setZonetype(java.lang.String zonetype) {
		this.zonetype = zonetype;
	}

	public java.lang.String getCreateby() {
		return createby;
	}

	public void setCreateby(java.lang.String createby) {
		this.createby = createby;
	}

	public java.lang.String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(java.lang.String createtime) {
		this.createtime = createtime;
	}

	public java.lang.String getUpdateby() {
		return updateby;
	}

	public void setUpdateby(java.lang.String updateby) {
		this.updateby = updateby;
	}

	public java.lang.String getUpdatetime() {
		return updatetime;
	}

	public void setUpdatetime(java.lang.String updatetime) {
		this.updatetime = updatetime;
	}

	public java.lang.String getRemark() {
		return remark;
	}

	public void setRemark(java.lang.String remark) {
		this.remark = remark;
	}

	public java.lang.String getDelflag() {
		return delflag;
	}

	public void setDelflag(java.lang.String delflag) {
		this.delflag = delflag;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}
	public java.lang.String getOrgno() {
		return orgno;
	}

	public void setOrgno(java.lang.String orgno) {
		this.orgno = orgno;
	}

	public String toString() {
		return new ToStringBuilder(this,ToStringStyle.MULTI_LINE_STYLE)
			.append("sid",getSid())
			.append("Zonecode",getZonecode())
			.append("Zonename",getZonename())
			.append("Zonetype",getZonetype())
			.append("Createby",getCreateby())
			.append("Createtime",getCreatetime())
			.append("Updateby",getUpdateby())
			.append("Updatetime",getUpdatetime())
			.append("Remark",getRemark())
			.append("Delflag",getDelflag())
			.append("Orgno",getOrgno())
			.toString();
	}

}

